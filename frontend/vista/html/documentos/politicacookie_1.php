  <p class="tit-pol-2">POLITICAS DE COOKIES</p>
<p class="subt-pol">¿Que son las cookies?</p>
Una cookie es un pequeño archivo colocado en tu computadora, smartphone u otro dispositivo electrónico que habilita las funcionalidades de Micamello. <br><br>

Por ejemplo, las cookies nos permiten identificar tu dispositivo, ofrecerte acceso seguro a Micamello, e incluso nos ayudan a saber si alguien intenta acceder a tu cuenta desde otro dispositivo. <br><br>

Las cookies también hacen seguimiento de la sesión de usuario, mejoran el tiempo de carga de una web, evitan mostrar información reiterativa y nos ayudan a mostrarte anuncios relevantes para ti.<br><br>

<br><br>
<p class="subt-pol">¿Por qué utilizamos cookies?</p>
Para mejorar tu experiencia de navegación como usuario, siendo la mejor manera de ofrecerte contenido personalizado y de interés a través del portal web y aplicación móvil. <br><br>

Asimismo, nos permiten ofrecerte publicidad relevante y basada en tus intereses, las cuales generan ingresos y nos permiten mantener la gratuidad de algunos servicios.<br><br>

<p class="subt-pol">¿Qué cookies utilizamos?</p>
Las cookies que utilizamos en la página web / aplicación móvil son las siguientes:  <br><br>
<ul class="numberLi">
  <li>
    <b>Autenticación:</b> Permiten mostrar la información adecuada y a personalizar tu experiencia y nos ayudan a determinar si el usuario ha accedido o no a la cuenta.
  </li>
  <li>
    <b>Funcilanidades y servicios: </b> Son esenciales para el correcto funcionamiento de los servicios. Proporcionan funcionalidades y contenidos personalizados.
  </li>
  <li>
    <b>Analíticas: </b> Permiten monitorizar el rendimiento de nuestros sitios webs y herramienta online. Por ejemplo: analizar patrones de comportamiento, número de usuarios que acceden, secciones visitadas, duración de la navegación, crear informes estadísticos sobre el tráfico web, audiencia global.
  </li>
</ul><br><br>

<p class="subt-pol">Control y desactivación de cookies</p>
Es posible desactivar y/o eliminar las cookies, siguiendo las indicaciones del navegador de Internet. Sin embargo, debe tenerse en cuenta que en este caso, se reducirá considerablemente tu capacidad para utilizar nuestros servicios e incluso hacerlos incompatibles con tus intereses o necesidades. <br><br>

Alternativas externas:<br><br>
<ul>
  <li>
    ‘Opt-out’ (desactivación) para cada tipo de cookies: el uso de este sistema puede instalar en tu computadora una cookie “de rechazo” para hacer efectiva la petición de desactivación de cookies solicitada.
  </li>
  <li>
    Otras herramientas de terceros disponibles online: te permiten detectar todas las cookies de cada web visitada y gestionar su desactivación.
  </li>
  <li>
    Sistemas “opt-out” disponibles para Google Analytics: <a href="https://tools.google.com/dlpage/gaoptout/">https://tools.google.com/dlpage/gaoptout/</a>

  <p class="tit-pol-2">POLITICAS DE COOKIES</p>
<p class="tit-pol-2">¿Que son las cookies?</p>
Las cookies son pequeños fragmentos de texto que se utilizan para almacenar información sobre
los navegadores web. Permiten almacenar y recibir identificadores e información adicional sobre
ordenadores, teléfonos y otros dispositivos. Otras tecnologías, incluidos los datos que
almacenamos en tu navegador web o dispositivo, los identificadores asociados a tu dispositivo y
otros programas, se utilizan con fines similares. A los efectos de esta política, todas estas
tecnologías reciben el nombre de "cookies".<br><br>
Las cookies permiten a Mi Camello ofrecerte sus promociones y nos ayudan a comprender la
información que recibimos de ti, incluidos los datos sobre el uso que realizas de otros sitios web,
independientemente de si estás o no registrado o si has iniciado sesión en la plataforma.<br><br>
<p class="tit-pol-2">¿Para que se utilizan las cookies?</p>
Las cookies nos ayudan a proporcionar, proteger y mejorar los Productos de Mi Camello, ya que
nos permiten personalizar el contenido, adaptar los anuncios y medir su rendimiento, y brindar
una experiencia más segura. Si bien las cookies pueden cambiar de vez en cuando a medida que
mejoramos y actualizamos los Productos de Mi Camello, las utilizamos con los fines que se
detallan a continuación:<br><br>

<p class="subt-pol">Autenticación</p>
<ul>
  <li>Utilizamos cookies para verificar tu cuenta y determinar cuándo inicias sesión. De este modo,
podemos ayudarte a acceder a los Productos de Mi Camello con mayor facilidad y mostrarte
la experiencia y las funciones correspondientes.
  <li>Utilizamos cookies para mantener tu sesión abierta mientras navegas en nuestra página. Las
cookies también nos ayudan a recordar tu navegador, de modo que no tengas que iniciar
sesión constantemente y puedas hacerlo con facilidad mediante aplicaciones y sitios web de
terceros.
  <li>Las cookies sirven para almacenar información que nos permita recuperar tu cuenta en caso
de que olvides tu contraseña o para solicitar autenticación adicional si nos indicas que
hackearon tu cuenta.
  <li>Para participar en ciertas promociones, como descuentos en planes, mejores ofertas de
trabajo, boletines informativos, etc., se le puede solicitar su nombre y dirección de correo
electrónico, además de que comparta cierta información concerniente a su educación y
preferencias de compra, entre otras. Esta información es para conocerlo mejor, ayudarlo a
entender nuevos servicios, programas y ofertas en las cuales usted pueda estar interesado, y
así mejorar su experiencia online.
  <li>Una vez que se haya registrado con nosotros, también rastreamos y recolectamos
información sobre usted incluyendo sus transacciones con nosotros, nuestros afiliados, y
otros; y los patrones que siguió usted al usar nuestro sitio web, como las páginas del sitio que
usted visitó, búsquedas que llevó a cabo y el tiempo que usted permaneció en nuestra página.</li></ul>
  <p class="subt-pol">Seguridad e integridad de los sitios y productos</p>
  <ul>
  <li>Utilizamos cookies para proteger tu cuenta, tus datos y los Productos de Mi Camello.</li>
<li>Las cookies pueden ayudarnos a determinar e imponer medidas adicionales de seguridad en
caso de que alguien intente acceder a una cuenta de Mi Camello sin autorización, por
ejemplo, adivinando rápidamente diferentes contraseñas.</li>
<li>Utilizamos cookies para proteger Mi Camello contra actividades que infringen nuestras
políticas o afectan de otro modo nuestra capacidad de proporcionar los Productos de Mi
Camello.</li>
<li>Las cookies nos ayudan a combatir el spam y los ataques de phishing, ya que nos permiten
identificar las computadoras que se utilizan para crear un gran número de cuentas falsas.</li>
<li>Utilizamos cookies para detectar las computadoras infectadas con malware y adoptar
medidas que impidan daños mayores.</li>
<li>Las cookies también nos ayudan a impedir que los menores de edad se registren.</li></ul>
<p class="subt-pol"> Publicidad, recomendaciones, estadísticas y medición</p>
<ul>
  <li>Utilizamos cookies para mostrar anuncios y hacer recomendaciones de empleos y a las
empresas / empleadores que pueden estar interesadas en tu perfil.</li>
  <li>Las cookies nos permiten entregar anuncios a personas que ya visitaron el sitio web,
compraron productos, así como recomendar empleos y empleadores. Asimismo, nos
permiten limitar el número de veces que se muestra un anuncio, de tal modo que no veas el
mismo anuncio una y otra vez.</li>
  <li>También utilizamos cookies para medir el rendimiento de las campañas publicitarias que usan
los Productos de Mi Camello.</li>
  <li>Utilizamos cookies para contar el número de veces que se muestra un anuncio y calcular su
costo.</li>
  <li>Utilizamos cookies para medir la frecuencia con la que se realizan determinadas acciones,
como hacer clic en un anuncio o verlo.</li>
  <li>Las cookies nos ayudan a mostrar y medir anuncios en diferentes navegadores y dispositivos
utilizados por la misma persona. Podemos utilizar cookies para impedir que veas el mismo
anuncio una y otra vez en los diferentes dispositivos que utilizas.</li>
  <li>Las cookies también nos permiten proporcionar estadísticas sobre las personas que utilizan
Mi Camello y sobre las personas que interactúan con los anuncios, los sitios web y las
aplicaciones de nuestros anunciantes y de los negocios que usan dichos Productos.</li>
  <li>Utilizamos cookies para ayudarte a indicar que no quieres ver anuncios de Mi Camello en
función de tu actividad en sitios web de terceros. </li>
  <li>Usted podrá visitar nuestro sitio web y usar algunos de nuestros servicios de manera
indetectable e Independientemente de que se haya registrado o no. Esto nos permite
recolectar información personal o relevante para el uso de nuestros servicios, como por
ejemplo el tipo de navegador, dirección IP, tipo de computadora o sistema operativo que usa,
la velocidad de su conexión, el nombre del dominio de su proveedor de servicio Internet, la
localidad geográfica desde dónde usted está accediendo a nuestros servicios.</li></ul>
<p class="subt-pol">Rendimiento</p>
<ul>
  <li>Las cookies nos ayudan a dirigir el tráfico entre los servidores y conocer con qué rapidez se
cargan los Productos de Mi Camello para cada persona.</li>
  <li>Las cookies sirven para registrar la relación de aspecto y las dimensiones de tu pantalla y tus
ventanas, y a saber si tienes activado el modo de contraste alto, para que podamos mostrar
correctamente nuestros sitios y aplicaciones.</li></ul>
<p class="subt-pol">Análisis e investigación</p>
<ul>
  <li>Las cookies nos ayudan a conocer cómo se utiliza el servicio de Mi Camello, analizar qué
partes de los Productos resultan más útiles e interesantes, e identificar funciones para
mejorar.</li>
</ul>
<p class="subt-pol">Administración de Cookies</p>
<ul>
  <li>Los navegadores permiten que usted elimine o bloquee las “cookies” de su computador o su
disco duro o generan unas alertas cuando estas se activen. Por favor refiérase a las
instrucciones de su Navegador para ayuda o para conocer mejor sus funciones. Tenga en
cuenta que, si configura su navegador para deshabilitar las cookies u otras tecnologías, es
posible que no pueda acceder a ciertas partes de nuestro sitio web o servicios, o que el sitio
no funcione correctamente. Para obtener más información acerca de las cookies, haga clic en
este enlace: http://www.aboutcookies.org/.</li></ul>
